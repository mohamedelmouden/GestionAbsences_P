    private void actionEmpAjouter() {
        String nom  = txtEmpNom.getText().trim();
        String dep  = txtEmpDepartement.getText().trim();
        String post = txtEmpPoste.getText().trim();
        if (nom.isEmpty() || dep.isEmpty()) { setStatut(lblEmpStatus, "Nom et département obligatoires", ACCENT_RED); return; }
        if (!new EmployeDAO().ajouter(new Employe(nom, dep, post))) { setStatut(lblEmpStatus, "Erreur BDD", ACCENT_RED); return; }
        actionEmpActualiser();
        viderChamps(txtEmpNom, txtEmpDepartement, txtEmpPoste);
        setStatut(lblEmpStatus, "Employé ajouté ✓", ACCENT_GREEN);
    }

    private void actionEmpModifier() {
        if (selEmp < 0) { setStatut(lblEmpStatus, "Sélectionnez une ligne", ACCENT_RED); return; }
        int id = (int) modelEmployes.getValueAt(selEmp, 3);
        String nom = txtEmpNom.getText().trim(), dep = txtEmpDepartement.getText().trim(), post = txtEmpPoste.getText().trim();
        if (!new EmployeDAO().modifier(new Employe(id, nom, dep, post))) { setStatut(lblEmpStatus, "Erreur BDD", ACCENT_RED); return; }
        actionEmpActualiser(); viderChamps(txtEmpNom, txtEmpDepartement, txtEmpPoste); selEmp = -1;
        setStatut(lblEmpStatus, "Employé modifié ✓", ACCENT_ORANGE);
    }

    private void actionEmpSupprimer() {
        if (selEmp < 0) { setStatut(lblEmpStatus, "Sélectionnez une ligne", ACCENT_RED); return; }
        if (JOptionPane.showConfirmDialog(this, "Supprimer cet employé ?", "Confirmation", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            int id = (int) modelEmployes.getValueAt(selEmp, 3);
            if (!new EmployeDAO().supprimer(id)) { setStatut(lblEmpStatus, "Erreur BDD", ACCENT_RED); return; }
            actionEmpActualiser(); viderChamps(txtEmpNom, txtEmpDepartement, txtEmpPoste); selEmp = -1;
            setStatut(lblEmpStatus, "Employé supprimé", ACCENT_RED);
        }
    }

    public void actionEmpActualiser() {
        if (modelEmployes == null) return;
        modelEmployes.setRowCount(0);
        List<Employe> l = new EmployeDAO().listerTous();
        for (Employe e : l) modelEmployes.addRow(new Object[]{e.getNom(), e.getDepartement(), e.getPoste(), e.getId()});
        if (lblEmpBadge != null) lblEmpBadge.setText(l.size() + " entrées");
        setStatut(lblEmpStatus, "Actualisé (" + l.size() + ")", ACCENT_GREEN);
    }

