    private void actionAbsAjouter() {
        try {
            Employe emp = (Employe) cmbEmploye.getSelectedItem();
            TypeAbsence t = (TypeAbsence) cmbType.getSelectedItem();
            if (emp==null||t==null){setStatut(lblAbsStatus,"Sélectionnez employé et type",ACCENT_RED);return;}
            Date dd=SDF.parse(txtAbsDebut.getText().trim()), df=SDF.parse(txtAbsFin.getText().trim());
            if (df.before(dd)){setStatut(lblAbsStatus,"Date fin avant date début!",ACCENT_RED);return;}
            if (!new AbsenceDAO().ajouter(new Absence(dd,df,emp.getId(),t.getId()))){setStatut(lblAbsStatus,"Erreur BDD",ACCENT_RED);return;}
            actionAbsActualiser(); setStatut(lblAbsStatus, "Absence ajoutée ✓", ACCENT_GREEN);
        } catch (Exception ex) { setStatut(lblAbsStatus, "Format date: yyyy-MM-dd", ACCENT_RED); }
    }

    private void actionAbsModifier() {
        if (selAbs<0){setStatut(lblAbsStatus,"Sélectionnez une ligne",ACCENT_RED);return;}
        try {
            int id=(int)modelAbsences.getValueAt(selAbs,7);
            Employe emp=(Employe)cmbEmploye.getSelectedItem(); TypeAbsence t=(TypeAbsence)cmbType.getSelectedItem();
            Date dd=SDF.parse(txtAbsDebut.getText().trim()), df=SDF.parse(txtAbsFin.getText().trim());
            if (!new AbsenceDAO().modifier(new Absence(id,dd,df,emp.getId(),t.getId()))){setStatut(lblAbsStatus,"Erreur BDD",ACCENT_RED);return;}
            actionAbsActualiser(); selAbs=-1; setStatut(lblAbsStatus,"Absence modifiée ✓",ACCENT_ORANGE);
        } catch (Exception ex){setStatut(lblAbsStatus,"Format date: yyyy-MM-dd",ACCENT_RED);}
    }

    private void actionAbsSupprimer() {
        if (selAbs<0){setStatut(lblAbsStatus,"Sélectionnez une ligne",ACCENT_RED);return;}
        if (JOptionPane.showConfirmDialog(this,"Supprimer cette absence ?","Confirmation",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) {
            int id=(int)modelAbsences.getValueAt(selAbs,7);
            if (!new AbsenceDAO().supprimer(id)){setStatut(lblAbsStatus,"Erreur BDD",ACCENT_RED);return;}
            actionAbsActualiser(); selAbs=-1; setStatut(lblAbsStatus,"Absence supprimée",ACCENT_RED);
        }
    }

